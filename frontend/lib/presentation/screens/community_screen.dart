            child: TabBarView(
              controller: _tabController,
              children: [
                _buildReviewsTab(),
                _buildJobTab(),
                _buildMarketTab(),
                _buildAuctionTab(),
              ],
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildReviewsTab() {
    return const ReviewBoardScreen();
